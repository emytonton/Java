package bootcamp.exercicios;

public class Operadoesunarios {
    public static void main(String[] args) {
        int numero = 5;

        numero = - numero; // para tornar negativo

        System.out.println(numero);

        numero = numero * -1; // para tornar um numero negativo em positivo, pois o "+" é visto como adição

        System.out.println(numero);

        int numero2 = 5;

        numero2++;

        System.out.println(numero2);

        int numero3 = 4;

        numero3--;

        System.out.println(numero3);

        boolean varialvel1 = true;

        varialvel1 = !varialvel1;

        System.out.println(varialvel1);




    }





    
}
